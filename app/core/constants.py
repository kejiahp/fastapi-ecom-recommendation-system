import random


class Constants:
    @staticmethod
    def randN(length: int):
        min_value = 10 ** (length - 1)
        max_value = 10**length - 1
        return random.randint(min_value, max_value)

    @staticmethod
    def random_gender_generator() -> str:
        """Generate a random gender, only male or female"""
        return random.choice(["male", "female"])

    @staticmethod
    def random_age_generator() -> int:
        """Generate a random age number within the range of 18 to 100"""
        return random.randint(18, 100)

    @staticmethod
    def random_rating_generator() -> int:
        """Generate a random rating number within the range of 1 - 5"""
        return random.randint(1, 5)

    @staticmethod
    def random_discount_type():
        """Generate a random discount type"""
        return random.choice(["UNIT", "FIXED"])

